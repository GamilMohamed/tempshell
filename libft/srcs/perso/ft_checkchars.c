#include "libft.h"

int	ft_checkchars(char c, char c1, char c2, char c3)
{
	if (c == c1 || c == '\t' || c == '\r' || c == '\v' || c == '\n'
		|| c == '\f')
		return (1);
	if (c == c2)
		return (2);
	if (c == c3)
		return (3);
	return (0);
}
