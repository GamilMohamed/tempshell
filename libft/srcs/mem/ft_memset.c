#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*bchar;

	bchar = (unsigned char *)b;
	while (len > 0)
	{
		bchar[len - 1] = c;
		len--;
	}
	return (b);
}
