#include "liblkriefft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int	z;
	char			c;

	z = (-(n < 0) + (n >= 0)) * n;
	if (n < 0)
		write(fd, "-", 1);
	c = '0' + z % 10;
	if (z >= 10)
		ft_putnbr_fd(z / 10, fd);
	write(fd, &c, 1);
}
